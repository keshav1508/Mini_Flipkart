    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try
        {
            Class.forName("oracle.jdbc.driver.OracleDriver");
            try (java.sql.Connection con = DriverManager.getConnection("jdbc:oracle:thin:@localhost:1521:xe","system","keshav15"); Statement st = con.createStatement()) {
                String qu = "select * from COURIER where COURIER_ID= ?";
                PreparedStatement pstmt=con.prepareStatement(qu);
                pstmt.setString(1,(jTextField1.getText()));

                ResultSet rs=pstmt.executeQuery();
                if(rs.next())
                {
                    jTextField1.setEditable(false);
                    jLabel2.setText(rs.getString("FULL_NAME"));
                    jLabel4.setText(rs.getString("COMPANY_NAME"));
                    jLabel5.setText(rs.getString("PHONE_NUMBER"));

                }
                else
                {
                    st.executeUpdate(qu);
                    JOptionPane.showMessageDialog(null,"record not found");
                }
            }

        }
        catch(HeadlessException | ClassNotFoundException | NumberFormatException | SQLException e)
        {
            JOptionPane.showMessageDialog(null,"NO SUCH RECORD FOUND");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

